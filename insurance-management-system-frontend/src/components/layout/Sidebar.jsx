/** @type {Record<string, NavItem[]>} */
const ROLE_LINKS = {
  CUSTOMER: CUSTOMER_LINKS,
  AGENT: AGENT_LINKS,
  ADMIN: ADMIN_LINKS,
}

/**
 * Sidebar
 *
 * Role-aware collapsible navigation sidebar rendered inside AppLayout.
 *
 * Props:
 *  @param {'ADMIN'|'AGENT'|'CUSTOMER'} role  - current authenticated user's role
 *  @param {boolean}  isOpen                  - whether the sidebar is currently visible
 *  @param {() => void} onClose               - callback to close the sidebar (mobile overlay)
 *
 * Behaviour:
 *  - Renders only the nav items for the given role.
 *  - On mobile it appears as a slide-in overlay drawer; clicking the backdrop calls onClose.
 *  - On large screens it is always visible as a fixed-width column (no overlay).
 *  - Active links are highlighted via NavLink's isActive class.
 *
 * Requirements: 3, 24
 */
function Sidebar({ role, isOpen, onClose }) {
  const links = ROLE_LINKS[role] ?? []

  return (
    <>
      {/* Mobile backdrop overlay */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black/30 z-20 lg:hidden"
          aria-hidden="true"
          onClick={onClose}
        />
      )}

      {/* Sidebar panel */}
      <aside
        className={[
          'fixed top-16 left-0 bottom-0 w-64 bg-white border-r border-gray-200 z-20',
          'flex flex-col overflow-y-auto',
          'transition-transform duration-200 ease-in-out',
          // Desktop: always visible; mobile: slide in/out
          'lg:translate-x-0',
          isOpen ? 'translate-x-0' : '-translate-x-full',
        ].join(' ')}
        aria-label="Main navigation"
      >
        <nav className="flex-1 py-4 px-3 space-y-1">
          {links.map((item) => (
            <NavLink
              key={item.to}
              to={item.to}
              onClick={onClose}
              className={({ isActive }) =>
                [
                  'flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors',
                  isActive
                    ? 'bg-blue-50 text-blue-700'
                    : 'text-gray-600 hover:bg-gray-100 hover:text-gray-900',
                ].join(' ')
              }
            >
              {item.icon}
              {item.label}
            </NavLink>
          ))}
        </nav>
      </aside>
    </>
  )
}

export default Sidebar
